#include "Double_Operations.h"

double Double_Add(double lh, double rh)
{
    return rh + lh;
}

double Double_Sub(double lh, double rh)
{
    return rh - lh;
}

double Double_Mul(double lh, double rh)
{
    return rh * lh;
}

double Double_Div(double lh, double rh)
{
    return rh / lh;
}